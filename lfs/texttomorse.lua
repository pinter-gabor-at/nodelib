	["@"] = ".--.-. ",
}


-- Convert morse code to blinky code
function M.tomorse(text)
	local morse = ""
	for i = 1, #text do
		local t = string.upper(string.sub(text, i, i))
		local m = xlat[t]
		if m then
			morse = morse .. m
		end
	end
	return morse
end


